// Generates email social icons: PNGs in public/email/ plus lib/email/icons.ts
// with base64 content for inline CID attachments. Emails can't use
// react-icons components, so we rasterize them once here.
import { createElement } from 'react'
import { renderToStaticMarkup } from 'react-dom/server'
import { FaLinkedinIn, FaGithub, FaXTwitter } from 'react-icons/fa6'
import sharp from 'sharp'
import { mkdirSync, readFileSync, writeFileSync } from 'node:fs'

const COLOR = '#475569' // slate-600 — reads as neutral on the white email card
const SIZE = 64 // rendered 2x+ for retina; displayed at ~22px

const icons = [
  { name: 'linkedin', Component: FaLinkedinIn },
  { name: 'github', Component: FaGithub },
  { name: 'x', Component: FaXTwitter },
]

mkdirSync('public/email', { recursive: true })

const entries = []
for (const { name, Component } of icons) {
  const svg = renderToStaticMarkup(createElement(Component, { size: SIZE }))
    .replace(/currentColor/g, COLOR)
  const path = `public/email/${name}.png`
  await sharp(Buffer.from(svg)).resize(SIZE, SIZE).png().toFile(path)
  entries.push(`  ${name}: '${readFileSync(path).toString('base64')}',`)
  console.log(`wrote ${path}`)
}

writeFileSync(
  'lib/email/icons.ts',
  `// Generated by scripts/gen-email-icons.mjs — do not edit by hand.
// Base64 PNGs attached to outgoing mail as inline CID images, so the
// icons render without depending on the site being deployed or on the
// client loading remote images.
export const EMAIL_ICONS: Record<'linkedin' | 'github' | 'x', string> = {
${entries.join('\n')}
}
`
)
console.log('wrote lib/email/icons.ts')
